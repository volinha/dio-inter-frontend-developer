//tipos primitivos

//boolean
var vf = true;
console.log(typeof(vf) + ': ' + vf);

//numero
var numero = 3;
console.log(typeof(numero) + ': ' + numero);

// string
var texto = "tudo bem?";
console.log(typeof(texto) + ': ' + texto);

var indefinida;
console.log(typeof(indefinida) + ": " + indefinida);
indefinida = "definida!"
console.log(typeof(indefinida) + ": " + indefinida);

let variavelDefinida = 10;
console.log(typeof(variavelDefinida) + ': ' + variavelDefinida);

const constante = 'sou constante!';
console.log(typeof(constante) + ': ' + constante);